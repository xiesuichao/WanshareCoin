package com.wanshare.crush.project.view;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;

import java.util.List;

/**
 * Created by xiesuichao on 2018/8/22.
 */

public class ProjectDetailVpAdapter extends FragmentPagerAdapter {

    private List<Fragment> mFragmentList;

    public ProjectDetailVpAdapter(FragmentManager fm, List<Fragment> fragmentList) {
        super(fm);
        this.mFragmentList = fragmentList;
    }

    @Override
    public Fragment getItem(int position) {
        return mFragmentList.get(position);
    }

    @Override
    public int getCount() {
        return mFragmentList.size();
    }
}
